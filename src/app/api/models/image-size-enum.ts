/**
 * The possible sizes of images. The actual pixel size depends on the configuration in Cyclos
 * Possible values are:
 * - `large`: Full image size
 * - `medium`: Medium thumbnail
 * - `small`: Small thumbnail
 * - `tiny`: Tiny thumbnail
 */
export enum ImageSizeEnum {
  LARGE = 'large',
  MEDIUM = 'medium',
  SMALL = 'small',
  TINY = 'tiny'
}
