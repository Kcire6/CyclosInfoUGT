/**
 * The reference level represents the satisfaction level
 * Possible values are:
 * - `bad`: Unsatisfied
 * - `good`: Satisfied
 * - `neutral`: Neutral
 * - `veryBad`: Very unsatisfied
 * - `veryGood`: Very satisfied
 */
export enum ReferenceLevelEnum {
  BAD = 'bad',
  GOOD = 'good',
  NEUTRAL = 'neutral',
  VERY_BAD = 'veryBad',
  VERY_GOOD = 'veryGood'
}
