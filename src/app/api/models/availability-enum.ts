/**
 * Determines the availability of a data
 * Possible values are:
 * - `disabled`: The data is disabled
 * - `optional`: The data is enabled and optional
 * - `required`: The data is enabled and required
 */
export enum AvailabilityEnum {
  DISABLED = 'disabled',
  OPTIONAL = 'optional',
  REQUIRED = 'required'
}
