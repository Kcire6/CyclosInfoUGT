/**
 * The days of the week
 * Possible values are:
 * - `fri`: Friday
 * - `mon`: Monday
 * - `sat`: Saturday
 * - `sun`: Sunday
 * - `thu`: Thursday
 * - `tue`: Tuesday
 * - `wed`: Wednesday
 */
export enum WeekDayEnum {
  FRI = 'fri',
  MON = 'mon',
  SAT = 'sat',
  SUN = 'sun',
  THU = 'thu',
  TUE = 'tue',
  WED = 'wed'
}
