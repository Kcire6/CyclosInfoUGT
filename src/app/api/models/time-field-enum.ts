/**
 * Determines a time field, such as seconds, hours or months
 * Possible values are:
 * - `days`: Day(s)
 * - `hours`: Hour(s)
 * - `millis`: Millisecond(s)
 * - `minutes`: Minute(s)
 * - `months`: Month(s)
 * - `seconds`: Second(s)
 * - `weeks`: Week(s)
 * - `years`: Year(s)
 */
export enum TimeFieldEnum {
  DAYS = 'days',
  HOURS = 'hours',
  MILLIS = 'millis',
  MINUTES = 'minutes',
  MONTHS = 'months',
  SECONDS = 'seconds',
  WEEKS = 'weeks',
  YEARS = 'years'
}
