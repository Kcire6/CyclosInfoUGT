/**
 * The existing user basic profile fields
 * Possible values are:
 * - `accountNumber`: Account number
 * - `address`: Address
 * - `email`: E-mail
 * - `image`: Image
 * - `name`: Full name
 * - `phone`: Phone (either mobile or land-line)
 * - `username`: Login name
 */
export enum BasicProfileFieldEnum {
  ACCOUNT_NUMBER = 'accountNumber',
  ADDRESS = 'address',
  EMAIL = 'email',
  IMAGE = 'image',
  NAME = 'name',
  PHONE = 'phone',
  USERNAME = 'username'
}
